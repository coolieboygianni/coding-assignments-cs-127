#This program uses a turtle to draw a flower


import turtle
 
 
tur = turtle.Turtle()

tur.speed(speed = 100)

for i in range(36):
  tur.forward(100)
  tur.left(145) 
  tur.forward(10)  
  tur.right(90) 
  tur.forward(10) 
  tur.left(135) 
  tur.forward(100)
